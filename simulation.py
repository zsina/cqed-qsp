def _build_initial_state(statetype, nbar_var, max_ph_var, custom_list):
    """Internal: construct the initial qubit–cavity state."""
    if statetype == 'Fock':
        return tensor(basis(2, 0), basis(max_ph_var, nbar_var))
    elif statetype == 'FockFromCoh':
        return tensor(
            basis(2, 0),
            coherent(max_ph_var, np.sqrt(nbar_var), offset=0, method='operator'),
        )
    elif statetype == 'Cat':
        return tensor(
            basis(2, 0),
            coherent(max_ph_var, np.sqrt(nbar_var), offset=0, method='operator'),
        )
    elif statetype == 'CustomFock':
        psi = 0 * tensor(basis(2, 0), basis(max_ph_var, 0))
        for shift in custom_list:
            psi = psi + tensor(basis(2, 0), basis(max_ph_var, nbar_var + shift))
        return psi / psi.norm()
    elif statetype == 'Fock_2_meas':
        return tensor(
            basis(2, 0),
            coherent(max_ph_var, np.sqrt(nbar_var), offset=0, method='operator'),
        )
    else:
        raise ValueError(f"Unknown statetype: '{statetype}'")


def _build_c_ops(diss_type, gamma_c, gamma_qr, gamma_qz, n_crit, max_ph_var):
    """Internal: construct collapse operators for the requested noise model."""
    if diss_type == 'CavOnly':
        return C_OPS(gamma_c, 0, 0, 0, n_crit, max_ph_var)
    elif diss_type == 'NoQDeph':
        return C_OPS(gamma_c, 0, gamma_qr, 0, n_crit, max_ph_var)
    elif diss_type == 'QDecOnly':
        return C_OPS(0, 0, gamma_qr, 0, n_crit, max_ph_var)
    elif diss_type == 'QDephOnly':
        return C_OPS(0, 0, 0, gamma_qz, n_crit, max_ph_var)
    elif diss_type == 'Full':
        return C_OPS(gamma_c, 0, gamma_qr, gamma_qz, n_crit, max_ph_var)
    else:
        raise ValueError(f"Unknown diss_type: '{diss_type}'")


def nice_sim_multiple(conv, num_list, output_list, cancel, arg_list,
                      custom_list, param_lib):
    """
    Sweep over photon numbers and run QSP measurement sequences.

    Parameters
    ----------
    conv : str
        'Wx' – use σ_x signal basis (Phase_to_time + H_full_reduced).
        'Wz' – use σ_z signal basis (Phase_to_time_Wz + H_full_reduced_Wz).
    num_list : array-like of int
        Target photon numbers to sweep.
    output_list : array-like of int
        Qubit measurement outcomes (0/1) for each round.
    cancel : str
        Cancellation mode: 'cancel_SQ', 'full_cancel', or 'half_cancel'.
    arg_list : list
        [diss_arg, statetype, diss_type]
        • diss_arg  : 'Diss' or 'NoDiss'
        • statetype : 'Fock', 'FockFromCoh', 'Cat', 'CustomFock', 'Fock_2_meas'
        • diss_type : 'CavOnly', 'NoQDeph', 'QDecOnly', 'QDephOnly', 'Full'
    custom_list : array-like
        Fock-number offsets (only used for statetype='CustomFock').
    param_lib : dict
        Must contain: 'chi', 'omega_con', 'gamma_c', 'gamma_qr',
                      'gamma_qz', 'n_crit'.

    Returns
    -------
    state_list : list of Qobj
        Final state after all measurements, one per nbar in num_list.
    state_2_list : list of Qobj
        Second-round states (only populated for statetype='Fock_2_meas').
    succ_list : list
        Final cumulative success probability per nbar.
    """
    diss_arg, statetype, diss_type = arg_list
    chi = param_lib['chi']
    omega_con = param_lib['omega_con']
    gamma_c = param_lib['gamma_c']
    gamma_qr = param_lib['gamma_qr']
    gamma_qz = param_lib['gamma_qz']
    n_crit = param_lib['n_crit']

    state_list = []
    state_2_list = []
    succ_list = []
    output_list = np.array(output_list)

    for nbar_var in num_list:
        max_ph_var = nbar_var + 100
        psi_0 = _build_initial_state(statetype, nbar_var, max_ph_var, custom_list)
        c_ops_var = _build_c_ops(diss_type, gamma_c, gamma_qr, gamma_qz,
                                  n_crit, max_ph_var)

        # Determine r and k lists
        if statetype == 'Fock_2_meas':
            params = rk_find_best_prime(nbar_var, 4)
            r_list = np.flip(params[1])
            k_list = np.flip(params[2])
        elif statetype == 'Cat':
            r_list = [np.ceil(np.sqrt(nbar_var))]
            k_list = [0]
        else:
            r_list = [nbar_var]
            k_list = [0]

        output_list_var = output_list.copy()
        psi_0_list = []

        for j in np.arange(len(r_list)):
            # For Fock_2_meas, flip outcomes on the second measurement round
            if len(output_list) % 2 == 1 and j == 1:
                output_list_var = (output_list_var + 1) % 2

            r_var = int(r_list[j])
            k_var = int(k_list[j])

            if conv == 'Wx':
                time_list = Phase_to_time(chi, omega_con, r_var, r_var, k_var)
                H_full = H_full_reduced(chi, omega_con, max_ph_var,
                                        nbar_var, cancel, 'Dispersive')
            elif conv == 'Wz':
                time_list = Phase_to_time_Wz(chi, omega_con, r_var, r_var, k_var)
                H_full = H_full_reduced_Wz(chi, omega_con, max_ph_var,
                                            nbar_var, cancel, 'Dispersive')
            else:
                raise ValueError(f"Unknown conv basis: '{conv}'")

            psi_r_test = multiple_meas(
                psi_0, time_list, output_list_var, H_full, c_ops_var, 'Diss'
            )
            psi_0 = psi_r_test[0][-1]
            psi_0_list.append(psi_0)

        state_list.append(psi_0_list[0])
        if statetype == 'Fock_2_meas':
            state_2_list.append(psi_0_list[1])
        succ_list.append(psi_r_test[1][-1])

    return state_list, state_2_list, succ_list


def nice_sim_multiple_rdep(nbar, r_list, output_list, cancel, args, param_lib):
    """
    Sweep over QSP polynomial degrees r for a fixed photon number nbar,
    using the Wz-basis.

    Parameters
    ----------
    nbar : int
        Fixed target photon number.
    r_list : array-like of int
        QSP degrees to sweep.
    output_list : array-like of int
        Measurement outcomes.
    cancel : str
        Cancellation mode.
    args : str
        'Diss' or 'NoDiss'.
    param_lib : dict
        System parameters (see nice_sim_multiple).

    Returns
    -------
    state_list : list of Qobj
    succ_list  : list of ndarray (cumulative probabilities per r)
    """
    chi = param_lib['chi']
    omega_con = param_lib['omega_con']
    gamma_c = param_lib['gamma_c']
    gamma_qr = param_lib['gamma_qr']
    gamma_qz = param_lib['gamma_qz']
    n_crit = param_lib['n_crit']

    max_ph_var = nbar + 100
    k_var = 0
    psi_0 = tensor(
        basis(2, 0),
        coherent(max_ph_var, np.sqrt(nbar), offset=0, method='operator'),
    )
    c_ops_var = C_OPS(gamma_c, 0, gamma_qr, gamma_qz, n_crit, max_ph_var)

    state_list = []
    succ_list = []

    for r_var in r_list:
        print(f'r = {r_var}')
        time_list = Phase_to_time_Wz(chi, omega_con, r_var, r_var, k_var)
        H_full = H_full_reduced_Wz(chi, omega_con, max_ph_var, nbar, cancel,
                                    'Dispersive')
        result = multiple_meas(psi_0, time_list, output_list, H_full,
                                c_ops_var, args)
        state_list.append(result[0][-1])
        succ_list.append(result[1])

    return state_list, succ_list


