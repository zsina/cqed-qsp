def Fock_prep(chi, omega_con, c_ops, psi_init, nbar, repeat, cancel, args):
    """
    Prepare a Fock state |nbar⟩ using the two-step CRT measurement protocol.

    Uses rk_find_best_prime to find coprime (r1, r2) and applies measurements
    sequentially to narrow the photon-number distribution.

    Parameters
    ----------
    chi, omega_con : float
        System parameters.
    c_ops : list of Qobj
        Collapse operators.
    psi_init : Qobj
        Initial state (e.g. coherent state in the cavity).
    nbar : int
        Target Fock number.
    repeat : int
        Number of QSP rounds per r value.
    cancel : str
        Cancellation mode.
    args : str
        'Diss' or 'NoDiss'.

    Returns
    -------
    psi_0_list : list of Qobj
    prob_list  : ndarray   (cumulative success probabilities)
    trace_dist : float
    fid        : float     (occupation probability of Fock state |nbar⟩)
    """
    from .measurement import number_dist
    max_ph = psi_init.dims[0][1]
    H_full = H_full_reduced(chi, omega_con, max_ph, nbar, cancel, 'Dispersive')

    psi_0 = psi_init
    psi_0_list = [psi_0]
    prob_list = [1]

    params = rk_find_best_prime(nbar, 3)
    r_list = np.flip(params[1])
    k_list = np.flip(params[2])

    for j in np.arange(len(r_list)):
        r = int(r_list[j])
        k = int(k_list[j])
        time_list = Phase_to_time(chi, omega_con, r, r, k)
        outcome_list = np.zeros(repeat)
        interm_state = multiple_meas(psi_0, time_list, outcome_list,
                                      H_full, c_ops, args)
        psi_0 = interm_state[0][-1]
        psi_0_list.append(psi_0)
        prob_list = np.append(prob_list, prob_list[-1] * interm_state[1][-1])

    from qutip import tracedist
    fid = number_dist(interm_state[0][-1], max_ph)[nbar]
    psi_target = tensor(basis(2, 0), basis(max_ph, nbar))
    psi_target = psi_target * psi_target.dag()
    trace_dist = tracedist(interm_state[0][-1], psi_target)
    return psi_0_list, prob_list, trace_dist, fid
