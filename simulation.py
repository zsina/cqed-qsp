"""
simulation.py
-------------
State preparation, QSP measurement sequences, and high-level simulation
drivers for the qubit–cavity cQED system.

All simulations assume the Hilbert space:   qubit ⊗ Fock(max_ph)

Qubit basis:  |0⟩ = |g⟩ (success),  |1⟩ = |e⟩ (failure)
"""

import numpy as np
from qutip import (
    basis, tensor, qeye, destroy, coherent,
    sigmax, sigmaz, expect,
    mesolve, sesolve,
)

from .pulses import (
    Phase_to_time, Phase_to_time_Wz,
    H_full_reduced, H_full_reduced_Wz,
    C_OPS,
)
from .number_theory import rk_find_best_prime


# ---------------------------------------------------------------------------
# State preparation
# ---------------------------------------------------------------------------

def prep_Coh_State(g_c, c_ops, max_ph, nbar):
    """
    Prepare an approximate coherent state |α⟩ (α = √nbar) in the cavity
    by driving a displacement Hamiltonian H = i g_c √nbar (a† - a) from
    the vacuum for time 1/g_c, with collapse operators c_ops.

    Parameters
    ----------
    g_c : float
        Displacement drive strength.
    c_ops : list of Qobj
        Collapse operators (can be empty for unitary displacement).
    max_ph : int
        Cavity Hilbert-space dimension.
    nbar : float
        Target mean photon number (|α|² = nbar).

    Returns
    -------
    Qobj
        Density matrix of the prepared state.
    """
    a = destroy(max_ph)
    psi_0 = tensor(
        basis(2, 0) * basis(2, 0).dag(),
        basis(max_ph, 0) * basis(max_ph, 0).dag(),
    )
    time_prep = np.linspace(0, 1 / g_c, 1000)
    result = mesolve(
        1j * g_c * np.sqrt(np.abs(nbar)) * tensor(qeye(2), a - a.dag()),
        psi_0,
        time_prep,
        c_ops,
    )
    return result.states[-1]


# ---------------------------------------------------------------------------
# Single QSP measurement
# ---------------------------------------------------------------------------

def qsp_meas(psi_0, time_list, H_full, c_ops, args):
    """
    Evolve psi_0 through one complete QSP pulse sequence.

    The time list is traversed segment by segment. Each segment uses either
    mesolve (open system, args='Diss') or sesolve (closed system).

    Parameters
    ----------
    psi_0 : Qobj
        Initial state (ket or density matrix).
    time_list : ndarray
        Ordered time markers generated by Phase_to_time[_Wz].
    H_full : list
        QuTiP time-dependent Hamiltonian.
    c_ops : list of Qobj
        Collapse operators (ignored for 'NoDiss').
    args : str
        'Diss'   – open-system simulation via mesolve.
        anything else – closed-system via sesolve.

    Returns
    -------
    Qobj
        State after the full pulse sequence.
    """
    for j in np.arange(len(time_list) - 1):
        t_start = time_list[j]
        t_stop = time_list[j + 1]
        if t_start == t_stop:
            continue
        t_span = np.linspace(t_start, t_stop, 100)
        solver_args = {'time_list': time_list}
        if args == 'Diss':
            result = mesolve(H_full, psi_0, t_span, c_ops, [], args=solver_args)
        else:
            result = sesolve(H_full, psi_0, t_span, args=solver_args)
        psi_0 = result.states[-1]
    return psi_0


# ---------------------------------------------------------------------------
# Repeated QSP measurements
# ---------------------------------------------------------------------------

def multiple_meas(psi_0, time_list, outcome_list, H_full, c_ops, args):
    """
    Apply len(outcome_list) sequential QSP measurements to psi_0,
    projecting onto the specified qubit outcome after each one.

    Parameters
    ----------
    psi_0 : Qobj
        Initial state.
    time_list : ndarray
        Pulse time markers.
    outcome_list : array-like of int
        Desired qubit measurement outcomes (0 = success, 1 = failure).
    H_full : list
        Time-dependent Hamiltonian.
    c_ops : list of Qobj
        Collapse operators.
    args : str
        'Diss' or anything else (see qsp_meas).

    Returns
    -------
    psi_out_list : list of Qobj
        Post-measurement states after each round.
    cumul_prob : ndarray
        Cumulative probability of the observed outcome sequence.
    """
    max_ph = psi_0.dims[0][1]
    succ = tensor(basis(2, 0) * basis(2, 0).dag(), qeye(max_ph))
    fail = tensor(basis(2, 1) * basis(2, 1).dag(), qeye(max_ph))

    cumul_prob = [1]
    psi_out_list = []

    for j in np.arange(len(outcome_list)):
        output_state = qsp_meas(psi_0, time_list, H_full, c_ops, args)
        print(f'  measurement {int(j)} complete')

        if outcome_list[j] == 0:
            prob = expect(succ, output_state)
            if args == 'Diss':
                post = succ * output_state * succ
            else:
                post = succ * output_state
        else:
            prob = expect(fail, output_state)
            if args == 'Diss':
                post = fail * output_state * fail
            else:
                post = fail * output_state

        post = post / post.norm()
        cumul_prob = np.append(cumul_prob, cumul_prob[-1] * prob)
        psi_out_list.append(post)
        psi_0 = post

    return psi_out_list, cumul_prob


